#ifndef __fe2k_asm2_md5_h__include__
#define __fe2k_asm2_md5_h__include__

#include <soc/sbx/fe2k_common/defs_vn.h>

/* md5 context structure */
typedef struct Md5c__ {
  unsigned long m_count[2], m_state[4]; /* number of bytes processed and intermediate digest state  */
  unsigned char m_buf[64], m_pad[64];/* data block being processed and buffer with the default padding */
}
Md5c;

/* md5 context setup */
int Md5c__init( Md5c *ctx );

/* md5 process buffer */
int Md5c__update( Md5c *ctx, unsigned char *input, int ilen );

/* md5 final digest */
int Md5c__finish( Md5c *ctx, unsigned char output[16] );

#endif /* __fe2k_asm2_md5_h__include__ */
